# PEP 8是最古老的PEP之一，它向Python程序员提供了代码格式设置指南。
# 2.缩进
# PEP 8建议每级缩进都使用四个空格，这既可提高可读性，又留下了足够的多级缩进空间。
# 3.行长
# 很多Python程序员都建议每行不超过80字符。PEP 8还建议注释的行长都不超过72字符
# 4.空行
# 要将程序的不同部分分开，可使用空行。你应该使用空行来组织程序文件，但也不能滥用；
