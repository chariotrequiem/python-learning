# 整数类型 简写为int，可以表示正数、负数和零
n1 = 90
n2 = -76
n3 = 0
print(n1, type(n1))
print(n2, type(n2))
print(n3, type(n3))
# 整数不同进制表示方法 十进制->默认的进制  二进制->以0b开头
# 八进制->以0o开头  十六进制->以0x开头
print('十进制', 118)
print('二进制', 0b10101111)
print('八进制', 0o10111)
print('十六进制', 0x1EAF)
