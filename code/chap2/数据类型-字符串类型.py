# 字符串类型又被称为不可变的字符序列
# 可以使用单引号''  双引号""  三引号''' ''' 或者""" """来定义
# 单引号和双引号定义的字符串必须在同一行
# 三引号定义的字符串可以分布在连续的多行
str1 = '你好，python'
print(str1, type(str1))
str2 = "你好，python"
print(str2, type(str2))
str3 = '''你好，python'''
print(str3, type(str3))
str4 = """你好，
python"""
print(str4, type(str4))
