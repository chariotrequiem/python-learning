# 1.标准算术运算符
print(1+1)  # 加法运算 +
print(1-1)  # 减法运算 -
print(2*4)  # 乘法运算 *
print(1/2)  # 除法运算 /
print(11//2)  # 整除运算 //

print(9//4)  # 2
print(-9//-4)  # 2
# 整除时一正一负向下取整
print(9//-4)  # -3
print(-9//4)  # -3
# 2.取余运算符
print(11 % 2)  # 取余运算 %
# 余数 = 被除数 - 除数 * 商(商即整除时的结果)
print(9 % -4)  # -3 = 9 - (-4)*(-3)
print(-9 % 4)  # 3 = -9 - 4*(-3)
# 3.幂运算
print(2**2)  # 表示的是2的2次方 **
print(2**3)  # 表示的是2的3次方 **

